#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] && s[i] != c)
		i++;
	if (!s[i] && c != s[i])
		return (NULL);
	return ((char *)&(s[i]));
}
